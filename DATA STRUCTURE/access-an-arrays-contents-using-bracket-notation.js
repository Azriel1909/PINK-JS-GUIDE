/**
 * In an array, each item has an index.
 * This index doubles as the position of that item in the array.
 * Js arrays are zero-indexed.
 * 
 * We can enclose an index in brackets and append it to the end
 * of the array (Bracket Notation).
 * 
 * let ourVariable = ourArray[0];
 * 
 * In addition to accessing the value associated with an index,
 * we can also set an index a value using the same notation.
 * 
 * let ourVariable = ourArray[0];
 * 
 */

