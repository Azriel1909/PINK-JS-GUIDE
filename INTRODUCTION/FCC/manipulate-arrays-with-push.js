// An easy way to append data to the end of an array is via the push() function.

const arr1 = [1, 2, 3];
arr1.push(4,5,6);
console.log(arr1);

const arr2 = ["Stimpson", "J", "cat"];
arr2.push(["happy", "joy"]);
console.log(arr2);

const myArray = [["John", 23], ["cat", 2]];
