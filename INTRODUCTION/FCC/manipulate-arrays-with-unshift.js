// With unshift() function we can add the element at the beginning of the array (it works exactly like push() function).

const ourArray = ["Stimpson", "J", "cat"];
ourArray.shift();
ourArray.unshift("Happy");
console.log(ourArray);

const myArray = [["John", 23], ["dog", 3]];
myArray.shift();
console.log(myArray);
myArray.unshift(['Paul', 35]);
console.log(myArray);


